console.log(document.querySelector(".container").className);   //returns all the class names.

console.log(document.querySelector(".container").classList);   //returns the list of class names

document.querySelector(".container").classList.add("prince");   //adds a  class named "prince" to the container.

document.querySelector(".container").classList.remove("prince");  //removed the class named "prince" from the container.


document.querySelector(".container").classList.toggle("red");  //toggled the class "red" i.e., means it has deactivated the "red" class.

document.querySelector(".container").classList.toggle("red");  //toggled the class "red" i.e., means it has reactivated the "red" class.