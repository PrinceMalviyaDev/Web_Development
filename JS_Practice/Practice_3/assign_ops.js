// learn assignment operators

let a = 10;  // "=" assignment operator

a += 5; //increments a by 5
console.log(a);  //15

a -= 5; //decrement a by 5
console.log(a);  //10

a *= 5; //multiply a by 5 and store in a itself
console.log(a);  //50

a /= 5; //divide a by 5 and store in a itself
console.log(a);  //10

a %= 4; //a modulo 4 and store in a itself
console.log(a);  //2

a **= 4; //a to the power 4 and store in a itself
console.log(a);  //16