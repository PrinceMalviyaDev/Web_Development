// Learn all the arithmetic operators

let a = 10;

a = a + 10;
console.log(a); // 20

a = a - 5;
console.log(a); // 15

a = a * 5;
console.log(a); // 75

a = a / 5;
console.log(a); //15

a = a % 4; 
console.log(a); //3

a = a ** 2; 
console.log(a); //9

a++;    // increment operator
console.log(a); //10

a--;    // decrement operator
console.log(a); //9

console.log(a++); //9   prints first then increment
console.log(a); //10   prints incremented value

console.log(a--); //10   prints first then decrement
console.log(a); //9    prints decremented value